#!/usr/bin/env python3

# =============================================================================
# merge_maf.py
# =============================================================================
#
# DESCRIPTION:
#   Merges one or more MAF (Mutation Annotation Format) files from a directory
#   into a single unified output table. The script produces three output files
#   targeting different audiences and use cases.
#
#   OUTPUT FILES:
#
#     - Final_result_tier1.maf
#         A standard MAF file containing all fields except those marked as
#         "drop" in the YAML config. No metadata second header row. Intended
#         as a clean, tool-compatible MAF file for downstream analysis.
#
#     - Final_result_tier2.tsv
#         A tab-separated file for bioinformaticians. Contains Tier 2 fields
#         as defined in Config.yaml (generated by build_config.py). Includes
#         a two-row header: field names on row 1, metadata (description,
#         source, version) on row 2.
#
#     - Final_result_tier3.tsv
#         A tab-separated file for clinical scientists. Contains Tier 3 fields
#         as defined in Config.yaml (generated by build_config.py). Includes
#         a two-row header: field names on row 1, metadata on row 2.
#         To change which fields appear here, update TIER3_FIELDS in
#         build_config.py and re-run it to regenerate Config.yaml.
#
#   Fields marked as "drop" in the YAML config are treated as redundant or
#   duplicate fields and are excluded from ALL output files without exception.
#
# INPUT:
#   - A directory containing one or more MAF files (.maf)
#   - A YAML configuration file (Config.yaml) generated by build_config.py,
#     which defines per-field metadata (description, source, version, tier)
#     and wildcard patterns for dynamically generated field groups such as
#     ONCOKB_DIAG_* and ONCOKB_TX_*
#
# OUTPUT:
#   - Final_result_tier1.maf   : all non-drop fields, single header, MAF format
#   - Final_result_tier2.tsv   : Tier 2 fields, two-row header
#   - Final_result_tier3.tsv   : Tier 3 fields, two-row header
#
# PROCESSING STEPS:
#   1.  Standardize FORMAT_DATA column name across all input files
#   2.  Expand FORMAT genotype fields (GT, AD, VAF, etc.) into separate columns
#   3.  Expand structured annotation columns (e.g. CIViC_CSQ) using pipe-
#       delimited subfield definitions from the YAML config
#   4.  Calculate End_Position based on variant type (SNV, indel, SV)
#   5.  Clean field values (replace empty arrays, URL-encoded characters, etc.)
#   6.  Compute VAF from allelic depth (AD) if not already present
#   7.  Drop any columns listed in COLS_TO_REMOVE
#   8.  Report columns not documented in the YAML config (warnings only)
#   9.  Remove all columns marked as "drop" in the YAML config
#   10. Write Tier 1 MAF file (single header, all remaining columns)
#   11. Split columns by tier (read from Config.yaml) and build two-row headers
#   12. Write Tier 2 and Tier 3 TSV files
#
# TIER DEFINITIONS (controlled by build_config.py, not this script):
#   Tier 1 : All non-drop fields — written to Final_result_tier1.maf
#   Tier 2 : ~400 bioinformatician fields — written to Final_result_tier2.tsv
#   Tier 3 : ~200 clinician fields — written to Final_result_tier3.tsv
#   Drop   : Excluded from all outputs
#
#   To change tier assignments, edit TIER2_FIELDS, TIER3_FIELDS, or DROP_FIELDS
#   in build_config.py and re-run it to regenerate Config.yaml.
#
# USAGE:
#   python merge_maf.py
#
#   Edit the entry point at the bottom of the script to configure:
#     - input_dir  : directory containing input MAF files
#     - output_dir : directory where output files will be written
#     - yaml_config: path to the generated Config.yaml file
#
# DEPENDENCIES:
#   - Python 3.8+
#   - pandas
#   - pyyaml
#
# RELATED FILES:
#   - ConfigORIGINAL.yaml : human-maintained field metadata template
#   - Config.yaml         : generated config consumed by this script
#   - build_config.py     : script that generates Config.yaml from the template
#
# AUTHOR:
#   Variant annotation pipeline — Manuel
# =============================================================================

